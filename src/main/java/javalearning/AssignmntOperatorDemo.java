package javalearning;
public class AssignmntOperatorDemo {
    public static void main(String[] args) {
        int x,y;
        x = 5;
        y = 2;
        
        x += y;
        System.out.println("x = " + x);
        
        x -= y;
        System.out.println("x = " + x);
        
        x *= y;
        System.out.println("x = " + x);
        
        x /= y;
        System.out.println("x = " + x);
        
        x %= y;
        System.out.println("x = " + x);
    }
}
