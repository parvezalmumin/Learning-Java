 public class DataTypesExample {
    public static void main(String[] args) {
        // float example (decimal number with 'f' at the end)
        float temperature = 36.6f;
        
        // long example (big integer with 'L' at the end)
        long population = 8000000000L; // world population ~ 8 billion
        
        // char example (single character in single quotes)
        char grade = 'A';
        
        // boolean example (true/false value)
        boolean isJavaFun = true;
        
        // Print values
        System.out.println("Body Temperature: " + temperature);
        System.out.println("World Population: " + population);
        System.out.println("Your Grade: " + grade);
        System.out.println("Is Java Fun? " + isJavaFun);
    }
}
